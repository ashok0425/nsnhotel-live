<p>As far as we know, hotels were a concept much removed from what we know nowadays. In India, at the end of the Middle Ages, the number of journeys being made increased, and so did the number of &ldquo;inns&rdquo;, that offered accommodation, food and drinks to travelers, although hygiene and privacy were not priorities. The quality was sensitively improved and the market grew during the Industrial Revolution. Nevertheless, the habit of integrating a private bathroom is quite modern. Only after private bathrooms were standardised in hotels, the detail of offering &laquo;free of charge&raquo; toiletries in the room were added.</p>
