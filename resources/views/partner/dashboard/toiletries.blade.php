<p>The value of details</p>
<p>Today we assume that, when entering the hotel room that has been assigned to us, we&rsquo;ll find reception products. They&rsquo;re number and quality are indicators of the establishment&rsquo;s standard of service. They are not necessarily aligned to the price of your overnight stay, but rather to the vocation of giving attention to details. It&rsquo;s this that makes your stay a premium experience or not.</p>
<p>Let&rsquo;s start by clarifying some terms:</p>
<ul>
<li><strong>Amenities</strong>are all those articles that a hotel puts at the client&rsquo;s disposal to improve their experience. This category includes notebooks and pen, sneakers, shoehorn, shoe cleaner sponge, tea service&hellip; up to wifi and movies free of charge, whatever direction the hotel thinks that the service will make his client loyal to the brand.</li>
